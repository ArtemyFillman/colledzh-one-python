# Программа обработки целого числа

# Ввод числа
print("Введите целое число:")
number = int(input())

# Определение знака числа
if number >= 0:
    # Прибавление 1
    number += 1
else:
    # Вычитание 3
    number -= 3

# Вывод результата
print("Результат обработки:", number)


