# Программа вычисления стоимости покупки с учетом скидки

# Ввод суммы покупки
print("Введите сумму покупки:")
summ = float(input())

# Проверка, предоставляется ли скидка
if summ > 1000:
    # Вычисление скидки
    discount = summ * 0.15
    # Вычисление стоимости покупки с учетом скидки
    cost = summ - discount
else:
    # Стоимость покупки без скидки
    cost = summ

# Вывод результатов
print("Стоимость покупки с учетом скидки составляет", cost, "рублей.")


